def get_P_matrix(num_car,local_cache):
    P = np.zeros([num_car,num_car])
    for i in range(num_car):
        for j in range(num_car):
            if i==j:
                continue;
            if j in local_cache[i]:
                P[i,j] = 1./(len(local_cache[i]))
    for i in range(num_car):
        P[i,i] = 1 - sum(P[i])
    return P


